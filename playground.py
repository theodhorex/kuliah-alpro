# Kegiatan Praktikum

a = int(input("Masukan nilai a: "))
b = int(input("Masukan nilai b: "))
c = int(input("Masukan nilai c: "))

if (a > b) and (a > c):
    print(a, "adalah bilangan terbesar!")
elif (b > a) and (b > c):
    print(b, "adalah bilangan terbesar!")
elif (c > a) and (c > b):
    print(c, "adalah bilangan terbesar!")
